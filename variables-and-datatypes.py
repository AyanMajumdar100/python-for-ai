# Basic variable types in Python

# Integer
age = 25

# Float
height = 5.10

# String
name = "Ayan"

# Boolean
is_student = True

# NoneType
middle_name = None

# Complex number
z = 3 + 4j

# Printing values and their types
print(age, type(age))
print(height, type(height))
print(name, type(name))
print(is_student, type(is_student))
print(middle_name, type(middle_name))
print(z, type(z))
